public class ChocolateRequestContainer {
    int smallBars;
    int bigBars;
    int desiredAmount;


    public int getSmallBars() {
        return smallBars;
    }
    public void setSmallBars(int smallBars) {
        this.smallBars = smallBars;
    }
    public int getBigBars() {
        return bigBars;
    }
    public void setBigBars(int bigBars) {
        this.bigBars = bigBars;
    }
    public int getDesiredAmount() {
        return desiredAmount;
    }
    public void setDesiredAmount(int desiredAmount) {
        this.desiredAmount = desiredAmount;
    }

}
